function q(prompt, a, b, c, d, correct, explain){
  const obj = {
    type: "single",
    prompt,
    answers: [
      { id: "a", text: a },
      { id: "b", text: b },
      { id: "c", text: c },
      { id: "d", text: d }
    ],
    correct
  };
  if (explain) obj.explain = explain;
  return obj;
}

function clamp(x, a, b){
  return Math.max(a, Math.min(b, x));
}

function shuffleSeeded(arr, seed){
  const a = [...arr];
  let state = ((Number(seed) || 1) * 9301 + 49297) % 233280;
  const rnd = () => (state = (state * 9301 + 49297) % 233280) / 233280;
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(rnd() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

