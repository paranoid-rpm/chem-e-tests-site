function clamp(x, a, b){
  return Math.max(a, Math.min(b, x));
}

function shuffle(arr, seed=null){
  const a = [...arr];
  if(seed === null){
    for(let i=a.length-1;i>0;i--){
      const j = Math.floor(Math.random()*(i+1));
      [a[i],a[j]]=[a[j],a[i]];
    }
    return a;
  }
  // простая детерминированная тасовка для повторяемости вариантов
  let s = (seed*9301 + 49297) % 233280;
  const rnd = () => (s = (s*9301 + 49297) % 233280) / 233280;
  for(let i=a.length-1;i>0;i--){
    const j = Math.floor(rnd()*(i+1));
    [a[i],a[j]]=[a[j],a[i]];
  }
  return a;
}

export function buildQuestions(test){
  const tpl = test?.template;
  const pool = bank[tpl] || [];
  const take = test?.questionCount || 10;
  const seed = Number(test?.seed || 1);

  if(!pool.length){
    return [];
  }

  // берём 10 вопросов вращением + перемешивание внутри пула
  const rotated = pool.map((x, idx) => ({x, idx}));
  const start = (seed - 1) % rotated.length;
  const seq = rotated.slice(start).concat(rotated.slice(0,start)).map(t => t.x);

  const chosen = seq.slice(0, clamp(take, 1, 25));
  const out = chosen.map((qq, i) => ({
    ...qq,
    id: `q${i+1}`,
    answers: qq.answers.map(a => ({...a}))
  }));

  // на уровне теста ответы будут дополнительно перемешаны в quiz.js
  return out;
}
